/**
  * @brief  通过 HAL 状态机发送字符串（阻塞模式）
  * @param huart: UART 句柄
  * @param str:   数据指针
  * @param len:   发送字节数
  */
void Usart_SendString(UART_HandleTypeDef *huart, uint8_t *str, uint16_t len)
{
  HAL_UART_Transmit(huart, str, len, HAL_MAX_DELAY);
}
/**
  * @brief  寄存器直写发送（仅用于故障异常处理器）
  * @note   不依赖 HAL 状态机，在 HardFault 等异常中 HAL 状态不可信时使用
  */
void Usart_SendString_Emergency(UART_HandleTypeDef *huart, uint8_t *str, uint16_t len)
{
  USART_TypeDef *uart = huart->Instance;
  for (uint16_t i = 0; i < len; i++) {
    while (!(uart->ISR & USART_ISR_TXE_TXFNF)) {}
    uart->TDR = str[i];
  }
  while (!(uart->ISR & USART_ISR_TC)) {}
}

/* HAL_UART_Receive_IT 使用的单字节接收缓冲区 */
static uint8_t rxByte1 = 0;
static uint8_t rxByte2 = 0;

/**
  * @brief  启动 UART1 / UART2 的 HAL 中断接收
  * @note   每收到 1 字节触发 HAL_UART_RxCpltCallback，入队后自动重新使能
  */
void UART_Receive_Init(void)
{
  HAL_UART_Receive_IT(&huart1, &rxByte1, 1);
  HAL_UART_Receive_IT(&huart2, &rxByte2, 1);
}

/**
  * @brief  HAL 接收完成回调：将收到的字节推入对应队列，并重新使能接收
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance == USART2) {
    osMessageQueuePut(CmdCtrlQueueHandle, &rxByte2, 0, 0);
    HAL_UART_Receive_IT(&huart2, &rxByte2, 1);
  } else if (huart->Instance == USART1) {
    osMessageQueuePut(MotorDataParseQueueHandle, &rxByte1, 0, 0);
    HAL_UART_Receive_IT(&huart1, &rxByte1, 1);
  }
}

/**
  * @brief  HAL 错误回调：HAL 已清除硬件错误标志，仅需重新启动接收
  */
void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance == USART1) {
    HAL_UART_Receive_IT(&huart1, &rxByte1, 1);
  } else if (huart->Instance == USART2) {
    HAL_UART_Receive_IT(&huart2, &rxByte2, 1);
  }
}

